export const ANNEXE_C_COMBINATION_COUNT = ANNEXE_C_ROWS.length;

const ANNEXE_C_BY_QUALIFYING_GROUPS = new Map(
  ANNEXE_C_ROWS.map(row => [[...row].sort().join(''), row]),
);

export const ANNEXE_C_UNIQUE_COMBINATION_COUNT = ANNEXE_C_BY_QUALIFYING_GROUPS.size;

export function getAnnexeCThirdPlaceGroups(
  qualifyingGroups: readonly string[],
): Record<(typeof ANNEXE_C_COLUMN_MATCH_IDS)[number], string> {
  const uniqueGroups = new Set(qualifyingGroups);
  if (qualifyingGroups.length !== 8 || uniqueGroups.size !== 8) {
    throw new Error('Annexe C requires exactly eight unique third-place groups.');
  }

  const key = [...uniqueGroups].sort().join('');
  const row = ANNEXE_C_BY_QUALIFYING_GROUPS.get(key);
  if (!row) {
    throw new Error(`No FIFA Annexe C option exists for qualifying groups ${key}.`);
  }

  return Object.fromEntries(
    ANNEXE_C_COLUMN_MATCH_IDS.map((matchId, index) => [matchId, row[index]]),
  ) as Record<(typeof ANNEXE_C_COLUMN_MATCH_IDS)[number], string>;
}
